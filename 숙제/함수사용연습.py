#연산을 하는 함수를 
#덧셈 : addtion 뺄셈 : 뺄셈은 subtraction 나눗셈: division 곱셈 :뺄셈은 subtraction 곱셈 : multiplication


def addtion(num1,num2):
    return num1+num2

print(addtion(2,3),'->덧셈결과')

def subtraction(num1,num2):
    return num1-num2
print(subtraction(5,3),'->뺄셈결과')    

def division(num1,num2):
    return num1/num2
print(division(6,3),'->나누기 결과')

def multiplication(num1,num2):
    return num1*num2

print(multiplication(2,3),'->곱셈결과')